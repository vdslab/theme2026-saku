from draw_torus import draw_torus


def test_draw_torus():
    """
    draw_torus関数のテスト
    通常エッジと逆方向エッジの両方を含むサンプルグラフを描画
    """
    # ノード集合
    V = [0, 1, 2, 3, 4, 5, 6, 7]

    # エッジ集合（通常エッジと逆方向エッジを含む）
    A = [
        (0, 2),  # レイヤー0 -> レイヤー1（通常）
        (0, 3),  # レイヤー0 -> レイヤー1（通常）
        (1, 4),  # レイヤー0 -> レイヤー2（通常）
        (2, 5),  # レイヤー1 -> レイヤー2（通常）
        (3, 6),  # レイヤー1 -> レイヤー3（通常）
        (4, 7),  # レイヤー2 -> レイヤー3（通常）
        (5, 1),  # レイヤー2 -> レイヤー0（逆方向、トーラス接続）
        (7, 2),  # レイヤー3 -> レイヤー1（逆方向、トーラス接続）
    ]

    # レイヤー集合（辞書形式）
    L = {
        0: [0, 1],  # レイヤー0にノード0, 1
        1: [2, 3],  # レイヤー1にノード2, 3
        2: [4, 5],  # レイヤー2にノード4, 5
        3: [6, 7],  # レイヤー3にノード6, 7
    }

    # 描画を実行
    draw_torus(V, A, L)


def test_draw_torus_simple():
    """
    シンプルなテストケース（逆方向エッジのみ）
    """
    V = [0, 1, 2]
    A = [
        (0, 1),  # 通常エッジ
        (1, 2),  # 通常エッジ
        (2, 0),  # 逆方向エッジ（トーラス接続）
    ]
    L = {
        0: [0],
        1: [1],
        2: [2],
    }

    draw_torus(V, A, L)


